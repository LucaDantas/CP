ll get(ll val, int sz, int b) {
    if(sz == 0) return 0;
    for(int l = b; l >= 0; l--) {
        if((1ll<<l) <= val) {
            ll aq = get(val - (1ll<<l), sz-1, l-1);
            if(aq != -1) return aq | (1ll<<l);
        }
    }
    return -1;
}

